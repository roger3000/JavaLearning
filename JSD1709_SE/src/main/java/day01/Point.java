package day01;
/**
 * 
 * 假设point 代表坐标系中的一个点
 * @author xhquan
 *
 */
public class Point {
	private int x;
	private int y;
	public Point() {
		
	}
	public Point(int x,int y) {
		this.x = x;
		this.y = y;
	}
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	
	
}
